#!/usr/bin/env node
'use strict';

/**
 * generate-unified-protocol.js — CLAUDE.md Protocol Generator
 *
 * Generates a unified code navigation protocol for CLAUDE.md based on
 * which cache layers and LSP providers are available.
 *
 * Usage:
 *   node scripts/generate-unified-protocol.js [project-dir]
 *
 * If project-dir is not specified, uses the current working directory.
 *
 * The script:
 *   1. Detects available cache layers (Codesight, OptiVault)
 *   2. Detects available LSP providers (cclsp, Serena)
 *   3. Generates a tiered navigation protocol
 *   4. Updates CLAUDE.md with the unified protocol (idempotent)
 */

const fs = require('fs');
const path = require('path');
const os = require('os');

// Import detection functions from the hooks lib
// Support both: running from source repo (../hooks/lib/) and installed (~/.claude/hooks/lib/)
const CLAUDE_DIR = path.join(os.homedir(), '.claude');
const libPaths = [
  path.join(__dirname, '../hooks/lib'),           // Source repo
  path.join(CLAUDE_DIR, 'hooks/lib'),             // Installed location
];

let detectCacheLayers, detectProviders, PROVIDERS;
for (const libPath of libPaths) {
  try {
    detectCacheLayers = require(path.join(libPath, 'read-cached-context')).detectCacheLayers;
    const providerLib = require(path.join(libPath, 'detect-lsp-provider'));
    detectProviders = providerLib.detectProviders;
    PROVIDERS = providerLib.PROVIDERS;
    break;
  } catch { /* try next path */ }
}

if (!detectCacheLayers || !detectProviders) {
  console.error('Error: Could not load hook libraries from:');
  libPaths.forEach(p => console.error(`  - ${p}`));
  process.exit(1);
}

const SENTINEL_START = '<!-- unified-nav-protocol -->';
const SENTINEL_END = '<!-- /unified-nav-protocol -->';

// Old sentinels/headers to remove (from individual tools)
// end: null means "remove from start to unified protocol sentinel or EOF"
const OLD_SENTINELS = [
  { start: '<!-- optivault-protocol -->', end: null },  // OptiVault sentinel
  { start: '# OptiVault Protocol Active', end: null },  // OptiVault header
  { start: '<!-- codesight-protocol -->', end: null },
  { start: '# AI Context (auto-generated by codesight)', end: null },
  { start: '# Project Context', end: null },  // Codesight's actual header
  { start: '<!-- lsp-enforcement-kit -->', end: '<!-- /lsp-enforcement-kit -->' },
];

function generateProtocol(projectDir) {
  const layers = detectCacheLayers(projectDir);
  const providers = detectProviders();

  const sections = [];

  // Header
  sections.push('# Code Navigation Protocol');
  sections.push('');
  sections.push('This project uses a tiered navigation system. Follow this order — cheapest first.');
  sections.push('');

  // Tier 1: Cached Context
  if (layers.codesight || layers.optivault) {
    sections.push('## Tier 1: Cached Context (free)');
    sections.push('Before any code exploration, check these files:');
    sections.push('');

    if (layers.optivault) {
      sections.push('**OptiVault** (file skeletons + dependency index):');
      sections.push('- `_optivault/_RepoMap.md` — master index of file exports and dependencies');
      sections.push('- `_optivault/<path>.md` — per-file skeleton with signatures (~50 tokens vs ~800 for full file)');
      sections.push('');
    }

    if (layers.codesight) {
      sections.push('**Codesight** (domain-level extraction):');
      sections.push('- `.codesight/routes.md` — HTTP routes with methods, paths, and middleware tags');
      sections.push('- `.codesight/schema.md` — data models with fields, types, and relations');
      sections.push('- `.codesight/components.md` — UI components with props');
      sections.push('- `.codesight/graph.md` — most-imported files and dependency graph');
      sections.push('');
    }

    sections.push('If the cached context answers your question, **stop here**.');
    sections.push('');
  }

  // Tier 2: LSP
  if (providers.length > 0) {
    sections.push('## Tier 2: LSP (accurate, costs a tool call)');
    sections.push('For questions the cache can\'t answer — references, call hierarchy, implementations, type hover — use LSP:');
    sections.push('');

    for (const key of providers) {
      const prov = PROVIDERS[key];
      if (!prov) continue;

      sections.push(`**${prov.label}**:`);
      sections.push('| Task | Tool |');
      sections.push('|------|------|');

      const tools = [
        { task: 'Find definition', intent: 'definition' },
        { task: 'Find references', intent: 'references' },
        { task: 'Symbol search', intent: 'symbol_search' },
        { task: 'Implementations', intent: 'implementation' },
        { task: 'Hover info', intent: 'hover' },
        { task: 'Diagnostics', intent: 'diagnostics' },
        { task: 'Incoming calls', intent: 'incoming_calls' },
        { task: 'Outgoing calls', intent: 'outgoing_calls' },
      ];

      for (const { task, intent } of tools) {
        const toolName = prov.tools[intent];
        if (toolName) {
          sections.push(`| ${task} | \`${prov.prefix}${toolName}\` |`);
        }
      }

      sections.push('');
    }

    sections.push('The enforcement hooks will block Grep/Glob/Read-before-LSP. Work with them, not around them.');
    sections.push('');
  } else {
    sections.push('## Tier 2: LSP');
    sections.push('No LSP MCP server detected. Install one of:');
    sections.push('- **cclsp** — https://github.com/ktnyt/cclsp (TypeScript/JavaScript)');
    sections.push('- **Serena** — https://github.com/oraios/serena (multi-language)');
    sections.push('');
  }

  // Tier 3: Direct File Access
  sections.push('## Tier 3: Direct File Access');
  sections.push('After consulting Tier 1 or Tier 2, read specific files at specific line ranges.');
  if (layers.optivault) {
    sections.push('Prefer reading the skeleton first (`_optivault/<path>.md`) to identify the exact lines you need.');
  }
  sections.push('');

  // After Write section
  if (layers.optivault || layers.codesight) {
    sections.push('## After Every Write');
    if (layers.optivault) {
      sections.push('- Call OptiVault\'s `sync_file_context` to update cached skeletons and RepoMap');
    }
    if (layers.codesight) {
      sections.push('- Codesight\'s semantic cache updates on next scan or via watch mode (`npx codesight --watch`)');
    }
    sections.push('- LSP language server updates automatically');
    sections.push('');
  }

  return sections.join('\n');
}

function removeOldSections(content) {
  let result = content;

  for (const { start, end } of OLD_SENTINELS) {
    if (end) {
      // Remove from start to end sentinel (inclusive)
      const regex = new RegExp(`${escapeRegex(start)}[\\s\\S]*?${escapeRegex(end)}\\n?`, 'g');
      result = result.replace(regex, '');
    } else {
      // Remove from start until our unified protocol sentinel or EOF
      // (these old sections don't have end markers, so we remove everything until we hit our own section)
      let startIdx = result.indexOf(start);
      while (startIdx !== -1) {
        const afterStart = result.slice(startIdx);
        // Look for our unified protocol sentinel
        const unifiedIdx = afterStart.indexOf(SENTINEL_START);
        if (unifiedIdx !== -1 && unifiedIdx > 0) {
          // Keep everything from unified sentinel onward
          result = result.slice(0, startIdx) + afterStart.slice(unifiedIdx);
        } else {
          // No unified section yet — remove to end
          result = result.slice(0, startIdx);
        }
        startIdx = result.indexOf(start);
      }
    }
  }

  // Remove our own old section
  const unifiedRegex = new RegExp(`${escapeRegex(SENTINEL_START)}[\\s\\S]*?${escapeRegex(SENTINEL_END)}\\n?`, 'g');
  result = result.replace(unifiedRegex, '');

  // Clean up multiple consecutive blank lines
  result = result.replace(/\n{3,}/g, '\n\n');

  return result.trim();
}

function escapeRegex(str) {
  return str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

function main() {
  const projectDir = process.argv[2] || process.cwd();
  const claudeMdPath = path.join(projectDir, 'CLAUDE.md');

  // Read existing CLAUDE.md or start fresh
  let existingContent = '';
  if (fs.existsSync(claudeMdPath)) {
    existingContent = fs.readFileSync(claudeMdPath, 'utf8');
  }

  // Remove old protocol sections
  const cleanedContent = removeOldSections(existingContent);

  // Generate new protocol
  const protocol = generateProtocol(projectDir);

  // Wrap in sentinels
  const wrappedProtocol = `${SENTINEL_START}\n${protocol}\n${SENTINEL_END}`;

  // Combine: existing content + new protocol
  const newContent = cleanedContent
    ? `${cleanedContent}\n\n${wrappedProtocol}\n`
    : `${wrappedProtocol}\n`;

  // Write back
  fs.writeFileSync(claudeMdPath, newContent);

  console.log(`Updated ${claudeMdPath}`);
  console.log('');
  console.log('Detected:');
  const layers = detectCacheLayers(projectDir);
  const providers = detectProviders();
  console.log(`  Codesight: ${layers.codesight ? 'yes' : 'no'} (CLI: ${layers.codesight_cli ? 'available' : 'not found'})`);
  console.log(`  OptiVault: ${layers.optivault ? 'yes' : 'no'} (CLI: ${layers.optivault_cli ? 'available' : 'not found'})`);
  console.log(`  LSP providers: ${providers.length > 0 ? providers.join(', ') : 'none'}`);
}

main();
